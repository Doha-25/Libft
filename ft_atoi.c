#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	answer;
	int	sign;

	i = 0;
	answer = 0;
	sign = 1;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (ft_isdigit(str[i]))
	{
		answer = answer * 10 + (str[i] - '0');
		i++;
	}
	return (answer * sign);
}
